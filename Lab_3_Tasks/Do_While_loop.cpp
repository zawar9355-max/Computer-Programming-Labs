#include <iostream>
using namespace std;

//Program to declare variable and print it's value 10 times.

int main() {
    int num = 5; // Declare and initialize the variable
    int count = 0; // Initialize a counter

    do {
        cout << "The value of num is: " << num << endl; // Print the value of num
        count++; // Increment the counter
    } while (count < 10); // Continue until the counter reaches 10

    return 0;
}
