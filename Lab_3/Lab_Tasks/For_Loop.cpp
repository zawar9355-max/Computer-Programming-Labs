#include <iostream>
using namespace std;
//Program to declare variable and print it's value 10 times.
int main() {
    int num = 5; // Declare and initialize the variable
    
    for (int i = 0; i < 10; i++) { // Loop to print the value 10 times
        cout << "The value of num is: " << num << endl; // Print the value of num
    }

    return 0;
}




